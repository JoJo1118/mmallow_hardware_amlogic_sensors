static tKeyLabel keys[NUM_KEYS];

static unsigned short dmpDefaultGetAddress( unsigned short key )
{
    static int isSorted = 0;
    if ( !isSorted ) {
        unsigned short kk;
        for (kk=0; kk<NUM_KEYS; ++kk) {
            keys[ kk ].addr = 0xffff;
            keys[ kk ].key = kk;
        }
        for (kk=0; kk<NUM_LOCAL_KEYS; ++kk) {
            keys[ dmpTConfig[kk].key ].addr = dmpTConfig[kk].addr;
        }
        isSorted = 1;
    }
    if ( key >= NUM_KEYS )
        return 0xffff;
    return keys[ key ].addr;
}


/**
 *  @brief
 *  @return ML_SUCCESS or a non-zero error code.
**/
tMLError dmpDefault(void)
{
    tMLError result;
    MLDLSetGetAddress( dmpDefaultGetAddress );

    result = MLDLClockSource( MPU_CLK_SEL_PLLGYROZ );
    ERROR_CHECK(result);
    result = MLDLCfgSamplingMPU( MPU_FILTER_42HZ, 4 );
    ERROR_CHECK(result);
    result = MLDLSetGyroPower( 1, 1, 1 );
    ERROR_CHECK(result);
    result = MLDLSetFullScaleMPU( 2000.f );
    ERROR_CHECK(result);
    result = MLDLLoadDMP( dmpMemory, SCD, sConfig );

    return result;
}
